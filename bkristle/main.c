#include "ft.h"

int main(int argc, char **argv)
{
	int i;

	i = 1;
	argc = argc + 0;
	if (argc == 1)
	{
		ft_input();
	}
	while (i < argc)
	{
		ft_read(argv[i]);
		i++;
	}
	return (0);
}



